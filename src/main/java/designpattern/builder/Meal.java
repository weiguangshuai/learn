package designpattern.builder;

/**
 * 建造者模式
 *
 * @author weigs
 * @date 2018/3/1 0001
 */
public class Meal {
    private String food;
    private String drink;

    public String getFood() {
        return food;
    }

    public void setFood(String food) {
        this.food = food;
    }

    public String getDrink() {
        return drink;
    }

    public void setDrink(String drink) {
        this.drink = drink;
    }
}
